#!/usr/bin/python3

def add(a, b):
    """um of a and b

    Args:
        a: first number
        b: second number

    Returns:
        the value a + b
    """

    return (a + b)


def sub(a, b):
    """subtraction of a and b

    Args:
        a: first number
        b: second number

    Returns:
        The value. a - b
    """
    return (a - b)


def mul(a, b):
    """multiplication of a and b

    Args:
        a: first number
        b: second number

    Returns:
        The value. a * b
    """
    return (a * b)


def div(a, b):
    """division of a and b

    Args:
        a: first number
        b: second number

    Returns:
        The value. a / b
    """
    return int(a / b)
